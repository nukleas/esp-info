export function getVariant(id) {
  return variants.find(v => v.id === id)
}

export function getVariantsByFeature(feature) {
  return variants.filter(v => {
    switch (feature) {
      case 'wifi6': return v.wireless.wifi6
      case 'bluetooth': return v.wireless.bluetooth !== null
      case 'thread': return v.wireless.thread
      case 'usb': return v.peripherals.usb
      case 'dualcore': return v.cpu.cores === 2
      default: return true
    }
  })
}
